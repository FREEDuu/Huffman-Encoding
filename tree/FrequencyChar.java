package tree;

public class FrequencyChar {

        public char c;
        public int freq;
             
        // A parameterized student constructor
        public FrequencyChar(char c, int freq) {
         
            this.c = c;
            this.freq = freq;
        }
         
        public int getFreq() {
            return this.freq;
        } 
        public char getC() {
            return c;
        } 
    }

